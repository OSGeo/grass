float IWave::equivwl() const
{
    float seb = 0;
    float wlwave = 0;

    for(int i = iinf; i <= isup; i++)
    {
	float sbor = ffu.s[i];
	if(i == iinf || i == isup) sbor *= 0.5;
	float wl = (float)(0.25 + i * step);
	float swl = solirr(wl);
	float coef = sbor * step * swl;
	seb += coef;
	wlwave += wl * coef;
    }

    return wlwave/seb;
}

